defmodule BRAN.Banks.C6Test do
  alias BRAN.Banks.C6

  use ExUnit.Case
  use ExUnit.Parameterized

  doctest BRAN.Banks.C6

  describe "BRAN.Banks.C6Test" do
    test_with_params "should return valid tuple when the account is valid",
                     fn branch, account, digit ->
                       assert C6.validate(branch, account, digit) == {:ok, :valid}
                     end do
      [
        {[0, 0, 0, 1], [1, 5, 3, 8, 5, 6, 8], 0},
        {[0, 0, 0, 1], [1, 7, 9, 2, 7, 0, 6], 4},
        {[0, 0, 0, 1], [1, 9, 1, 6, 5, 3, 7], 4},
        {[0, 0, 0, 1], [1, 9, 6, 8, 6, 0, 8], 0},
        {[0, 0, 0, 1], [1, 9, 8, 9, 1, 8, 2], 2},
        {[0, 0, 0, 1], [2, 0, 9, 0, 2, 8, 1], 6},
        {[0, 0, 0, 1], [2, 1, 1, 9, 7, 9, 4], 6},
        {[0, 0, 0, 1], [2, 3, 0, 7, 7, 5, 4], 9},
        {[0, 0, 0, 1], [2, 3, 5, 2, 6, 0, 5], 0},
        {[0, 0, 0, 1], [2, 3, 8, 6, 5, 6, 4], 4},
        {[0, 0, 0, 1], [2, 4, 6, 3, 8, 0, 3], 0},
        {[0, 0, 0, 1], [2, 6, 1, 7, 9, 0, 3], 2},
        {[0, 0, 0, 1], [2, 6, 5, 0, 2, 2, 7], 5},
        {[0, 0, 0, 1], [2, 7, 3, 9, 4, 3, 0], 1},
        {[0, 0, 0, 1], [2, 7, 6, 5, 1, 1, 1], 8},
        {[0, 0, 0, 1], [2, 9, 6, 1, 3, 7, 5], 2},
        {[0, 0, 0, 1], [2, 9, 8, 0, 0, 3, 3], 1},
        {[0, 0, 0, 1], [3, 1, 1, 4, 4, 0, 3], 9},
        {[0, 0, 0, 1], [3, 1, 3, 5, 3, 1, 1], 8},
        {[0, 0, 0, 1], [3, 1, 5, 7, 8, 0, 6], 3},
        {[0, 0, 0, 1], [3, 3, 6, 6, 2, 5, 6], 8},
        {[0, 0, 0, 1], [3, 4, 9, 4, 1, 8, 6], 0},
        {[0, 0, 0, 1], [3, 5, 2, 4, 3, 3, 6], 8},
        {[0, 0, 0, 1], [3, 5, 7, 6, 3, 4, 0], 0},
        {[0, 0, 0, 1], [3, 6, 8, 2, 0, 7, 6], 8},
        {[0, 0, 0, 1], [3, 7, 0, 4, 6, 8, 8], 8},
        {[0, 0, 0, 1], [3, 7, 6, 6, 7, 7, 3], 4},
        {[0, 0, 0, 1], [3, 7, 9, 2, 3, 3, 0], 7},
        {[0, 0, 0, 1], [3, 9, 8, 2, 3, 3, 0], 0},
        {[0, 0, 0, 1], [4, 0, 1, 3, 8, 0, 0], 3},
        {[0, 0, 0, 1], [4, 1, 2, 1, 4, 3, 5], 8},
        {[0, 0, 0, 1], [4, 1, 7, 6, 9, 8, 1], 3},
        {[0, 0, 0, 1], [4, 6, 6, 6, 9, 5, 3], 1},
        {[0, 0, 0, 1], [4, 9, 2, 4, 1, 4, 5], 1},
        {[0, 0, 0, 1], [1, 3, 5, 0, 1, 4, 5], 3},
        {[0, 0, 0, 1], [3, 0, 6, 1, 1, 2], 4},
        {[0, 0, 0, 1], [3, 2, 4, 3, 4, 0], 0},
        {[0, 0, 0, 1], [3, 3, 1, 8, 8, 3], 4},
        {[0, 0, 0, 1], [3, 7, 0, 3, 0, 3], 7},
        {[0, 0, 0, 1], [4, 3, 4, 3, 8, 9], 1},
        {[0, 0, 0, 1], [7, 3, 6, 9, 3, 4], 4},
        {[0, 0, 0, 1], [7, 7, 2, 7, 9, 5], 0},
        {[0, 0, 0, 1], [7, 7, 9, 0, 1, 3], 9},
        {[0, 0, 0, 1], [1, 3, 5, 0, 1, 4], 5},
        {[0, 0, 0, 1], [1, 4, 8, 7, 8], 4},
        {[0, 0, 0, 1], [2, 4, 9, 7, 9], 3},
        {[0, 0, 0, 1], [7, 3, 7, 9, 3], 3}
      ]
    end

    test_with_params "should return :invalid_bank_branch tuple when the branch is not valid",
                     fn branch, account, digit ->
                       assert C6.validate(branch, account, digit) ==
                                {:error, :invalid_bank_branch}
                     end do
      [
        {[4, 3, 1, 3], [4, 3, 1, 2, 9], 9},
        {[7, 0, 6, 8], [6, 0, 5, 2, 8], 8},
        {[4, 3, 0, 7], [9, 0, 1, 3, 3], 7},
        {[4, 2, 5, 8], [5, 8, 7, 8, 3], 6},
        {[1, 6, 6, 1], [3, 3, 9, 1, 7], 5},
        {[4, 2, 7, 5], [7, 4, 5, 8, 8], 4},
        {[6, 2, 5, 4], [6, 5, 4, 0, 4], 2},
        {[7, 9, 2, 8], [2, 3, 8, 4, 9], 2},
        {[1, 3, 8, 2], [2, 5, 2, 0, 7], 1},
        {[8, 5, 4, 9], [7, 4, 0, 1, 1], 1}
      ]
    end

    test_with_params "should return :not_valid tuple when the account is not valid",
                     fn branch, account, digit ->
                       assert C6.validate(branch, account, digit) == {:error, :not_valid}
                     end do
      [
        {[0, 0, 0, 1], [4, 3, 1, 2, 9], 9},
        {[0, 0, 0, 1], [6, 0, 5, 2, 8], 8},
        {[0, 0, 0, 1], [9, 0, 1, 3, 3], 7},
        {[0, 0, 0, 1], [5, 8, 7, 8, 3], 6},
        {[0, 0, 0, 1], [3, 3, 9, 1, 7], 5},
        {[0, 0, 0, 1], [7, 4, 5, 8, 8], 4},
        {[0, 0, 0, 1], [6, 5, 4, 0, 4], 2},
        {[0, 0, 0, 1], [2, 3, 8, 4, 9], 2},
        {[0, 0, 0, 1], [2, 5, 2, 0, 7], 1},
        {[0, 0, 0, 1], [7, 4, 0, 1, 1], 1}
      ]
    end
  end
end
